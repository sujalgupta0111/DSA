package com.code;

public class ListNode {
	public int val;
	public int count;
	public ListNode next;

	public ListNode() {
	}

	public ListNode(int val) {
		this.val = val;
		this.count = count;
	}
	public ListNode(int val,int count) {
		this.val = val;
		this.count = count;
	}

	public ListNode(int val, ListNode next) {
		this.val = val;
		this.next = next;
	}
}
